/**
 * Find number associated with color.
 * @param {String} color 
 */
export const colorCode = (color) => 
  COLORS.indexOf(color.toLowerCase().trim())

export const COLORS = [
  'black',
  'brown',
  'red',
  'orange',
  'yellow',
  'green',
  'blue',
  'violet',
  'grey',
  'white',
];
